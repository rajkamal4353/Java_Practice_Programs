package com.java_practice;

public class Nested_For_Loop_Decrement {
	
	   public static void main(String[] args) {
		   
         for (int i = 5; i >= 0; i--) {
    	   
              for (int j = 3; j >= 0; j--) {
		      System.out.println(i+" "+j);
	          }
              
	     System.out.println();
         }
         
       }
}